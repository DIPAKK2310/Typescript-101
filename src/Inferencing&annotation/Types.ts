
//Inference -- When we declare a variable and assign a value to it, TypeScript infers the type of the variable based on the assigned value.
//Inshort it get value from the variable itselt
let drink = "water"
console.log(drink)

console.log(typeof drink)

//output ==> string








//Annotation -- When we explicitly specify the type of a variable, function parameter, or return type, we are using type annotations.
//Inshort we give type to the variable
let food: string = "23"
console.log(food)

console.log(typeof food)

//output ==> string